#include <iostream>
#include <vector>
#include <string>
#include <algorithm>
#include <cmath>



int main() {
    int a = 30;
    int b = 20;
    int c = 10;

    std::cout << a << std::endl;
    std::cout << b << std::endl;
    std::cout << c << std::endl;

    std::cout << "a + b + c = " << a + b + c << std::endl;
    std::cout << "a * b * c = " << a * b * c << std::endl;
    std::cout << "a - b - c = " << a - b - c << std::endl;
    std::cout << "a / b / c = " << a / b / c << std::endl;
}